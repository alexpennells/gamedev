package pacman;

/**
 * Specifies the different directions that link and stalfos' can be moving.
 * 
 * @author Alex Pennells
 * @since 2013-01-27
 */
public enum Direction
{
    /**
     * The game object is moving or facing up.
     */
    UP,
    
    /**
     * The game object is moving or facing down.
     */
    DOWN,
    
    /**
     * The game object is moving or facing right.
     */
    RIGHT,
    
    /**
     * The game object is moving or facing left.
     */
    LEFT;
}
